public class Main {
	public static void main(String[] args) {
	 int i = 5; //Nilai awal 5
	    System.out.println("awal: " + i);
	    System.out.println("\nNilai Pre-increment: ");
	    System.out.println("++i: " + ++i + "(i sekarang: " + i + ")");
	  
	    System.out.println("i++: " + i++ + "(i sekarang: " + i + ")");
	  
	    System.out.println("\nNilai Pre-decrement");
	    System.out.println("--i: " + --i + "(i sekarang: " + i + ")");
	  
	    System.out.println("i--: " + i-- + "(i sekarang: " + i + ")");
	}
}

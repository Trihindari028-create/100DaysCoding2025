public class Main {
    public static void main(String[] args) {
        // Deklarasi variabel
        int angka;
        String nama;
        
        // Inisialisasi variabel
        int angka = 40;
        String nama = "Dunia Boba";
        
        // Menampilkan nilai awal variabel
        System.out.println("Nilai awal angka: " + angka);
        System.out.println("Nilai awal nama: " + nama);
        
        // Update variabel
        angka = 20;
        nama = "Dunia Boba";
        
        // Menampilkan nilai variabel setelah update
        System.out.println("Nilai angka setelah update: " + angka);
        System.out.println("Nilai nama setelah update: " + nama);
    }
}

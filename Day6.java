public class Main {
    public static void main(String[] args) {
        
        float angkaFloat = 3.141f;
        System.out.println("Nilai float: " + angkaFloat);
        
        double angkaDouble = 3.173;
        System.out.println("Nilai double: " + angkaDouble);
        
        float hasilFloat = angkaFloat * 2;
        double hasilDouble = angkaDouble * 2;
      
        System.out.println("Hasil float * 2: " + hasilFloat);
        System.out.println("Hasil double * 2: " + hasilDouble);
    }
}

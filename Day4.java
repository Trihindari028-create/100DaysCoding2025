public class Main {
    public static void main(String[] args) {
        System.out.println("Halo\nindar!");  // \n membuat baris baru
        System.out.println("Nama\t: indar");  // \t menambahkan tab
        System.out.println("Ini adalah tanda backslash: \\");  // \\ menampilkan \
        System.out.println("indar berkata: \"Selamat malam!\"");  // \" menampilkan tanda kutip ganda
        System.out.println("Karakter apostrof: \'");  // \' menampilkan tanda kutip tunggal
    }
}

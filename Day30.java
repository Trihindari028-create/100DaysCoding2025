public class Main {
	public static void main(String[] args) {
	      int angka1 = 5;
        int angka2 = 10;
        boolean hasilKurangsama = angka1 <= angka2;
        System.out.println("Apakah " + angka1 + " <= " + angka2 + "? " + hasilKurangsama);  // Output: true
        boolean hasilLebihsama = angka1 >= angka2;
        System.out.println("Apakah " + angka1 + " >= " + angka2 + "? " + hasilLebihsama);  // Output: false
    }
}
